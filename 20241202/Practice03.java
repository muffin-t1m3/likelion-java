public class Practice03 {
    public static void main(String[] args) {
        int width = 12;
        int height = 6;

        for (int i = 0; i < height; i++) {
            for (int j = 0; j < width; j++) {
                System.out.print("*");
            }
            System.out.print("\n");
        }
    }
}
